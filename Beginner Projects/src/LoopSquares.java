/** Prints numbers, with for loops.
  * @author Indiana Davis
  * @version October 10, 2013
  */

//Runs the loop.
public class LoopSquares{

/** Outputs a line of numbers to the console using for loops. 
  * @param args not used, ignored. */
  
   public static void main(String[] args){
   //Sets the for loop.
      for (int i = 1; i <= 10; i++){
         System.out.print((i * i));
         System.out.print(" ");
      }
   }
}
      